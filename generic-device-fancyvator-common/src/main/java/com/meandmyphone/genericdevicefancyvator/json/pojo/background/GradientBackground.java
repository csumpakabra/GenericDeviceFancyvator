package com.meandmyphone.genericdevicefancyvator.json.pojo.background;

public class GradientBackground extends Background {


    private int topLeftColor, topRightColor, botRightColor, botLeftColor;

    public int getTopLeftColor() {
        return topLeftColor;
    }

    public void setTopLeftColor(int topLeftColor) {
        this.topLeftColor = topLeftColor;
    }

    public int getTopRightColor() {
        return topRightColor;
    }

    public void setTopRightColor(int topRightColor) {
        this.topRightColor = topRightColor;
    }

    public int getBotRightColor() {
        return botRightColor;
    }

    public void setBotRightColor(int botRightColor) {
        this.botRightColor = botRightColor;
    }

    public int getBotLeftColor() {
        return botLeftColor;
    }

    public void setBotLeftColor(int botLeftColor) {
        this.botLeftColor = botLeftColor;
    }
}
